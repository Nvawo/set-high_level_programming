#!/usr/bin/python3
def add(a, b):
    """Returns the addition of a and b."""
    return a + b


def sub(a, b):
    """Returns the subtraction of a and b."""
    return a - b


def mul(a, b):
    """Returns the multiplication of a and b."""
    return a * b


def div(a, b):
    """Returns the integer division of a and b."""
    return int(a / b)
